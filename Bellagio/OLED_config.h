void fb_draw_string_big (unsigned char x, unsigned char y, const char *pS)
{
    unsigned int k;

    while(*pS)
    {
        for(k=0; k<10; k++)
        {
            buffer[( y    << 7) + x + k] = font2[*pS - ' '][k*2  ];
            buffer[((y+1) << 7) + x + k] = font2[*pS - ' '][k*2+1];
        }
        
        x += 10;

        pS++;
    }    
}

