void IdleI2C(void){
    while ((SSPSTAT & 0x04) || (SSPCON2 & 0x1F)); //Transmisi?on en progreso.
}
void WriteI2C(unsigned char data){
    IdleI2C();
    SSPBUF = data; //Write data to SSPBUF
}

unsigned char ReadI2C(void){
    IdleI2C();
    RCEN = 1; // Enable Receive mode for I2C.
    IdleI2C();
    return SSPBUF; //Read data from SSPBUF
}

void AckI2C(void){
    SSPCON2bits.ACKDT=0; // Will acknowledge.
    SSPCON2bits.ACKEN=1; // Send the acknowledge bit.
}

void NotAckI2C(void){
    SSPCON2bits.ACKDT=1; // Will not acknowledge.
    SSPCON2bits.ACKEN=1; // Send the not acknowledge bit.
}
void OpenI2C(void){
    SSPSTAT &= 0x3F; // power on state
    SSPCON1 = 0x00; // power on state
    SSPCON2 = 0x00; // power on state
    SSPCON1 |= 0b00001000; // select serial mode
    SSPSTAT |= 0b10000000; // slew rate on/off
    TRISBbits.RB0 = 1;
    TRISBbits.RB1 = 1;
    SSPCON1 |= 0b00100000; // enable synchronous serial port
    SSPADD = 0x13;
}

void StartI2C(void){
    IdleI2C();
    SEN = 1; // Lleva a cabo la condici?on de inicio.
}

void StopI2C(void){
    IdleI2C();
    PEN = 1; // Lleva a cabo la condicion de parada.
}
    
void WriteI2CDev(unsigned char SlaveAdress, unsigned char Register, unsigned char* Data, unsigned char Length){
    IdleI2C(); // Wait until the bus is idle
    StartI2C(); // Send START condition
    IdleI2C(); // Wait for the end of the START condition
    WriteI2C( SlaveAdress & 0xfe ); // Send address with R/W#
    // cleared for write
    IdleI2C(); // Wait for ACK
    WriteI2C( Register ); // Send address of register where data will
    // be written.
    IdleI2C(); // Wait for ACK
    for(int i=0; i<Length; i++) {
        WriteI2C(Data[i]); // Write first byte of data
        IdleI2C(); 
    } // Wait for ACK
    StopI2C(); // Hang up, send STOP condition
}

void ReadI2CDev(unsigned char SlaveAdress, unsigned char Register, unsigned char* Data, unsigned char Length){
    IdleI2C(); // Wait until the bus is idle
    StartI2C(); // Send START condition
    IdleI2C(); // Wait for the end of the START condition
    WriteI2C(SlaveAdress&0xfe); // Send address with R/W# cleared for write
    IdleI2C(); // Wait for ACK
    WriteI2C(Register); // Send address of register to be read.
    IdleI2C(); // Wait for ACK
    StartI2C(); // Send START condition
    IdleI2C(); // Wait for the end of the START condition
    WriteI2C(SlaveAdress|0x01); // Send address with R/W set for read
    IdleI2C(); // Wait for ACK
    for(int i=0; i<Length; i++){
        Data[i] = ReadI2C(); // Read byte of data
        if(i != (Length-1)){
            AckI2C(); // Send ACK if we are going to continue reading.
        }
        else{
            NotAckI2C(); // Send NACK if we won?t continue reading.
        }
    }
    StopI2C(); // Hang up, send STOP condition
}

